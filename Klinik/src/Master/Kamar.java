    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
        // TODO add your handling code here:
        try{
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID Kamar");
            model.addColumn("Nama");
            model.addColumn("Jumlah Kamar");
            model.addColumn("Jumlah Terpakai");
            model.addColumn("Harga");
            model.addColumn("Status");
            String cari = txt_cari.getText();
            String sql = "SELECT * FROM kamar WHERE id_kamar LIKE '%"+cari+"%' OR nama LIKE '%"+cari+"%' OR jumlah_kamar LIKE '%"+cari+"%' OR harga LIKE '%"+cari+"%' OR status LIKE '%"+cari+"%' ORDER BY id_kamar";
            java.sql.Connection conn=(Connection)Koneksi.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6)});
            }
            jTable1.setModel(model);
            txt_cari.setText(null);
        }catch(Exception ex){
            Component rootPane = null;
            JOptionPane.showMessageDialog(rootPane, "Data yang dicari tidak ada !!!!");

        }
    }//GEN-LAST:event_btn_cariActionPerformed

