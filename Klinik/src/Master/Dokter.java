    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        button_tabelklik();
        int baris = jTable1.rowAtPoint(evt.getPoint());
        String id_dokter =jTable1.getValueAt(baris, 0).toString();
        txt_idDokter.setText(id_dokter);
        txt_idDokter.setEditable(false);
        String nama = jTable1.getValueAt(baris,1).toString();
        txt_nama.setText(nama);

        String s=jTable1.getValueAt(baris, 2).toString();
        try{
            SimpleDateFormat f=new SimpleDateFormat("yyyy-MM-dd");
            java.util.Date d=f.parse(s);
            txt_tanggalLahir.setDate(d);
        }catch(ParseException ex){
        }

        String jenis_kelamin = jTable1.getValueAt(baris, 3).toString();
        txt_jenisKelamin.setSelectedItem(jenis_kelamin);
        String alamat=jTable1.getValueAt(baris, 4).toString();
        txt_alamat.setText(alamat);
        String no_hp = jTable1.getValueAt(baris, 5).toString();
        txt_noHp.setText(no_hp);
        String spesialis = jTable1.getValueAt(baris, 6).toString();
        txt_spesialis.setText(spesialis);
        String tarif = jTable1.getValueAt(baris, 7).toString();
        txt_tarif.setText(tarif);
    }//GEN-LAST:event_jTable1MouseClicked

