    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
        // TODO add your handling code here:
        try{
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID Dokter");
            model.addColumn("Nama");
            model.addColumn("Tanggal Lahir");
            model.addColumn("Jenis Kelamin");
            model.addColumn("Alamat");
            model.addColumn("No Hp");
            model.addColumn("Spesialis");
            model.addColumn("Tarif");
            String cari = txt_cari.getText();
            String sql = "SELECT * FROM dokter WHERE id_dokter LIKE '%"+cari+"%' OR nama LIKE '%"+cari+"%' OR tanggal_lahir LIKE '%"+cari+"%' OR jenis_kelamin LIKE '%"+cari+"%' OR alamat LIKE '%"+cari+"%' OR no_hp LIKE '%"+cari+"%' OR spesialis LIKE '%"+cari+"%' ORDER BY id_dokter";
            java.sql.Connection conn=(Connection)Koneksi.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6),res.getString(7),res.getString(8)});
            }
            jTable1.setModel(model);
            txt_cari.setText(null);
        }catch(Exception ex){
            Component rootPane = null;
            JOptionPane.showMessageDialog(rootPane, "Data yang dicari tidak ada !!!!");

        }
    }//GEN-LAST:event_btn_cariActionPerformed


